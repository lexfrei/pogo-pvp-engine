// CPMAt returns the canonical Combat Power Multiplier for the given level.
// Level must be in [MinLevel, MaxLevel] on a 0.5 grid; otherwise CPMAt
// returns ErrInvalidLevel. The XL flag has no effect on CPMAt — the table
// itself encodes levels above NoXLMaxLevel.
func CPMAt(level float64) (float64, error) {
	if math.IsNaN(level) || level < MinLevel || level > MaxLevel {
		return 0, fmt.Errorf("%w: %.2f outside [%.1f, %.1f]", ErrInvalidLevel, level, MinLevel, MaxLevel)
	}

	doubled := level * 2
	if doubled != math.Trunc(doubled) {
		return 0, fmt.Errorf("%w: %.2f is not on the 0.5 grid", ErrInvalidLevel, level)
	}

	index := int(doubled) - 2

	return cpmTable[index], nil
}
